#include<stdio.h>
int main(){
	int a=8, b=4;
	
	printf("%d\n",a+b);//It add
	printf("%d\n",a-b);//It sub
	printf("%d\n",a/b);//It Divide
	printf("%d\n",a*b);//It Multiplye
	printf("%d\n",a%b);//It give Quotient
	printf("%d\n",a++);//add 1 number
	printf("%d\n",a--);//sub 1 number
	
	return 0;
}